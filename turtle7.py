import turtle


turtle.title("Exemplo de Imagem Preenchida")
turtle.setup(400, 400)
turtle.pencolor('orange')
turtle.fillcolor('navy')
turtle.begin_fill()
turtle.forward(150); turtle.left(90)
turtle.forward(150); turtle.left(90)
turtle.forward(150); turtle.left(90)
turtle.forward(150)
turtle.end_fill()
turtle.done()